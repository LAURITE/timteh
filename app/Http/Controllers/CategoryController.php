     */
    public function store(Request $request)
    {
        $requestData=$request->all();
        $fileName=time().$request->file('cphoto')->getClientOriginalName();
        $path=$request->file('cphoto')->storeAs('images', $fileName, 'public');
        $requestData["cphoto"]='/storage/'.$path;
        Category::create($requestData);
 return redirect('category');
    }

